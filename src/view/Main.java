    private void importaArquivoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importaArquivoButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        fileChooser.setFileFilter(new SetFileFilter()); //especifica a extenção
        int estadoDeSelecao = fileChooser.showOpenDialog(this); 
        if (estadoDeSelecao == JFileChooser.CANCEL_OPTION) {
            return; 
        }
        File arquivoSelecionado = fileChooser.getSelectedFile();        
        if (presenter.esUmArquivoValido(arquivoSelecionado)) {
            try {            
                presenter.processarDadosDoArquivo(arquivoSelecionado);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Erro no arquivo","Erro no arquivo", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nome inválido","Nome inválido", JOptionPane.ERROR_MESSAGE);
        }
        presenter.atualizarTabelaDeContato("");
    }//GEN-LAST:event_importaArquivoButtonActionPerformed

